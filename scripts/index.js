function compareRange(unit1, unit2) {
  if (unit1.range < unit2.range) {
    return -1;
  }
  if (unit2.range < unit1.range) {
    return 1;
  }
  return 0;
}

function compareNames(unit1, unit2) {
  if (unit1.unit < unit2.unit) {
    return -1;
  }
  if (unit2.unit < unit1.unit) {
    return 1;
  }
  return 0;
}
